                <h1>Facebook, Instagram or Pinterest.  What’s the best choice to market your brand online? </h1>

                <!-- Author -->
                <p class="lead">
                    by <!-- <a href="#"> -->Nuwanthika Dharmaratne</a>
                </p>

                <hr>

                <!-- Date/Time -->
                <p><span class="glyphicon glyphicon-time"></span> Posted on April 23, 2017 at 10:00 AM</p>

                <hr>

                <!-- Preview Image -->
                <img class="img-responsive" src="../img/blog_post4_main.jpg" alt="">

                <hr>

                <!-- Post Content -->
                <p class="lead">It has now become a fact that social media is the most effective tool to market any brand’s products and services. Even the most traditional businesses have moved to the digital platform to increase brand awareness and sales.  The number and variety of social media channels are increasing by the day and the biggest concern brand managers and social media marketers are facing today is choosing the best platform to invest in.  However, there is no right or wrong answer to this concern. The best channel to market on depends on many factors which we will be discussing below briefly. </p>

                <h4>1.	What you are selling or providing</h4>
                <p>Your main product or service will determine a specific Target Group that you should strive to capture as a business. It is important that you choose a channel that entertains a large proportion of your target audience. 
				For example, in the Sri Lankan context, most F&B related businesses have found great success on Instagram while online stores do much better on Facebook. At the same time, businesses that offer corporate services have a massive advantage when using LinkedIn for their marketing purposes and artists and creative solutions providers also find Pinterest to be a great tool to carry their business forward. 
 				</p>
                

                <h4>2.	What your ultimate objective is</h4>
                <p>This is something most people overlook, because it seems so obvious. The reason for marketing is to increase sales. This is actually quite true. However, it is important to understand that the percentage of sales that can be converted by a mere social media campaign is highly limited. Conversion rates are always the hardest factors to deal with when considering digital marketing, because the number of variables contributing to it is quite different. Therefore, there has to be a more defined objective of your marketing campaign. It could be to generate leads, increase brand awareness or to introduce a new element of your business.  Once the objective is defined, you can consider what channel will be best for you. For example, Facebook might be the best tool to create leads, while Instagram might work better for brand awareness.  Some businesses have also started using Snapchat as one of their main marketing tools. However, the market segment that uses Snapchat in Sri Lanka can be highly limited. </p>


                <h4>3.	How much of a budget you have </h4>
                <p>This is a critical factor when advertising on social media. Anyone who has run a campaign online understands that it’s almost impossible to gain customer interaction without pushing your material using at least a small budget. This mainly applies to the initial stage of a business. Once an adequate subscribership is build, the need for boosting and paid advertisements may reduce.  </p>

                <p>Therefore, it’s best to talk to an experienced professionals in the industry and to get a feel of how much you will have to spend through the different approaches and make a sound decision on the way to go. </p>


                
                
                
                
